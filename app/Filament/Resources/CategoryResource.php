                SelectTree::make('parent_id')
                    ->label('Parent Category')
                    ->enableBranchNode()
                    ->withCount()
                    ->emptyLabel('Oops, no results have been found!')
                    ->relationship('parent', 'name', 'parent_id')
                    ->searchable(),

                Forms\Components\Textarea::make('description')
                    ->maxLength(65535)
                    ->columnSpanFull(),
            ]);
